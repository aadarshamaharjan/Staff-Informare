    private void check(){
        String np=jPasswordField1.getText();
        String rnp=jPasswordField2.getText();
        String pass=jPasswordField3.getText();
        String upin=pin;
        if(pass.equals(upin)){
            if(!np.equals(rnp))
                JOptionPane.showMessageDialog(null,"PIN doesn't match!","ALERT",JOptionPane.ERROR_MESSAGE);
            else{
                String sql1= "DELETE FROM user_login";
                String sql= "INSERT INTO user_login(PinCode)VALUES('"+np+"')";
                try{
                    stmt.execute(sql1);
                    stmt.execute(sql);
                    System.out.println("Data Inserted");
                }catch(Exception e){
                    System.out.println("Data not inserted" + e);
                }
                JOptionPane.showMessageDialog(null,"PIN changed succesfully!","Success",JOptionPane.PLAIN_MESSAGE);
                db.dbconreset();
                dispose();
            }
        }
        else{
            JOptionPane.showMessageDialog(null,"Old Pin doesn't match!","ALERT",JOptionPane.ERROR_MESSAGE);
        }
    }
