    private void beep(java.awt.event.KeyEvent evt,String str){
        char c=evt.getKeyChar();
        if(!(Character.isDigit(c) || (c==KeyEvent.VK_BACK_SPACE) || (c==KeyEvent.VK_DELETE)) || str.length()>3){
            getToolkit().beep();
            evt.consume();
        }
    }
